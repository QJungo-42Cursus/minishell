#include "str.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	unsigned int	res_len;
	unsigned int	i;
	unsigned int	j;
	char			*res;

	res_len = ft_strlen(s1) + ft_strlen(s2);
	res = malloc(sizeof(char) * (res_len + 1));
	if (res == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		res[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		res[i] = s2[j];
		i++;
		j++;
	}
	res[i] = 0;
	return (res);
}
